#include <iostream>

int a = 5; // Declares a variable to hold the INTEGER of 5, inside the variable called 'a'.
int b = 20; // Declares a variable to hold the INTEGER of 20, inside the variable called 'b'.

int total = a + b; // stores the sum of the two variables a and b and stores them in 'total'


using namespace std; //With this line, we can get rid of having to type 'std' everytime we use it.

int main()
{
	cout << total << endl; // This will output the data inside the variable ' total' out to the screen.
	system("pause");
return 0;
}